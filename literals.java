class literals
{
	public static void main(String[] args)
	{
		int i = 10;
		float f=20.6f;
		double d = 68.32;
		char c = 'A';
		String s = "NFC";
		long l=2734;
	System.out.println("int =" +i);
	System.out.println("float =" +f);
	System.out.println("long =" +l);
	System.out.println("double =" +d);
	System.out.println("char =" +c);
	System.out.println("string =" +s);

	}




}